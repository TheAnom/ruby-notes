# Operadores

# Operadores aritméticos
puts 5 + 3    # Suma
puts 10 - 2   # Resta
puts 4 * 2    # Multiplicación
puts 8 / 2    # División
puts 10 % 3   # Módulo (resto de la división)
puts 2 ** 3   # Exponenciación (2 elevado a la 3)

# Operadors relacionales
puts 5 == 5   # Igualdad
puts 5 != 3   # Desigualdad
puts 5 > 3    # Mayor que
puts 5 < 3    # Menor que
puts 5 >= 5   # Mayor o igual que
puts 5 <= 3   # Menor o igual que

# Operadores lógicos
puts true && false  # AND lógico
puts true || false   # OR lógico
puts !true          # NOT lógico

# Asi mismo se pueden combinar operadores
puts (5 > 3) && (2 < 4)  # AND lógico con comparación
puts (5 == 5) || (3 != 2) # OR lógico con comparación   

